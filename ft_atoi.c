#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	n;
	int	sign;
	int	i;

	sign = 1;
	i = 0;
	n = 0;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			sign = -1;
		i++;
	}
	while (nptr[i] && nptr[i] >= '0' && nptr[i] <= '9')
	{
		n = (nptr[i] - '0') + n * 10;
		i++;
	}
	return (n * sign);
}
